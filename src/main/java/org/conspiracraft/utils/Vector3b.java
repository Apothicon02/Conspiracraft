package org.conspiracraft.utils;

public class Vector3b {
    public boolean x, y, z;
    public Vector3b() {
        this.x = false;
        this.y = false;
        this.z = false;
    }
    public Vector3b(boolean x, boolean y, boolean z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
